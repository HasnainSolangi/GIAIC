//Subtraction Operator

// Initial amount of money you have
let moneyIHave: number = 2000;

// Prices of different fruits
let bananaPrice: number = 500;
let mangoPrice: number = 1000;
let peachPrice: number = 300;

// Calculate the total cost of fruits
let totalCost: number = bananaPrice + mangoPrice + peachPrice;

// Calculate the remaining money after buying fruits
let remainingMoney: number = moneyIHave - totalCost;

// Display the remaining money
console.log("Remaining money after buying fruits: " + remainingMoney + " rupees");
